package aula_calc;

public class Calculo {
	public double soma(double v1, double v2){
		double total;
		total = v1 + v2;
		return total;
	}
	public double subtracao(double v1, double v2){
		double total;
		total = v1 - v2;
		return total;
	}
	public double multiplicacao(double v1, double v2){
		double total;
		total = v1 * v2;
		return total;
	}
	public double divisao(double v1, double v2){
		double total;
		total = v1 / v2;
		return total;
	}
}